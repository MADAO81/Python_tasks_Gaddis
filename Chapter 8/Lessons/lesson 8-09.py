# This program demonstrates "split" method 

def main():
    # create string value with some words
    my_string = """If you like to gamble, I tell you I'm your man 
    You win some, lose some, all the same to me"""
    
    # split string value
    word_list = my_string.split()
    
    # print result
    print(word_list)
    
# call main function
main()