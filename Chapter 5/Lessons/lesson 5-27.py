# The 'rectangle' module contains functions that perform 
#  calculations related to rectangles

# The 'area' function takes the width and length of a rectangle 
# as arguments, and returns the area of the rectangle.

def area(width,length):
    return width*length
    
# The 'perimeter' function takes the width and length of a rectangle 
# returns the perimeter of the rectangle as arguments.
def perimeter(width, length):
    return 2 * (width + length)
    
