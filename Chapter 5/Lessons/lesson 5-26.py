# The module 'circle' contains functions that perform calculations related to circles.
import math

# Function 'area' take takes the radius of the circle as of the argument and returns the area of the circle.
def area(radius):
    return math.pi * radius**2
    
# The 'circumference' function takes the radius of a circle
# and returns the length of the circle.
def circumference(radius):
    return 2 * math.pi * radius
    
